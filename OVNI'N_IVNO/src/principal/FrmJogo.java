    @Override
    public void run() {

        //Posicionar elementos
        j.setX(450);
        j.setY(350);
        e.setY(700);
        a.setX(220);
        a.setY(250);
        o.setX(375);
        o.setY(230);

        //Inicializar controles
        proporcao = 300;
        pontuacao = 0;
        tempo = 0;
        melhorPontuacao = 0;

        while (true) {
            g = getBufferStrategy().getDrawGraphics();
            g.setColor(Color.WHITE);

            if (!fimJogo) {
                b.desenhar(g);
                a.desenhar(g);
                g.setFont(new Font("Arial", 0, 19));
                g.drawString("Seja bem-vindo ao jogo...", 370, 180);
                g.drawString("Pressione ESPA√áO para iniciar", 340, 570);
                g.drawString("Desenvolvido por: Gustavo Lu√≠s A. Ferreira RA: 160037", 480, 680);
                g.setFont(new Font("Arial", 3, 22));
                g.drawString("OVNI'N IVNO", 405, 220);
            }

            if (iniciarJogo) {
                //Desenha background
                b.desenhar(g);
                //Desenha jogador
                j.desenhar(g);

                //Move jogador
                j.voar();

                //Gera valor randomico
                r = new Random();

                //Recebe valor randomico que perten√ßa ao valor de propor√ß√£o
                int q = (int) r.nextInt(proporcao);
                if (q >= 0 && q <= 4) {
                    //Cria meteoro
                    Meteoro m = new Meteoro();

                    //Gera meteoro em determinada coordenada de acordo com o valor randomico
                    switch (q) {
                        case 0:
                            m.setIncX(-2);
                            m.setIncY(2);
                            m.setImagem("img/meteoro-descendo-esquerda.gif");
                            m.setY(30);
                            break;
                        case 1:
                            m.setIncY(2);
                            m.setIncX(2);
                            m.setImagem("img/meteoro-descendo-direita.gif");
                            m.setY(30);
                            break;
                        case 2:
                            m.setIncY(-2);
                            m.setIncX(2);
                            m.setImagem("img/meteoro-subindo-esquerda.gif");
                            m.setY(600);
                            break;
                        case 3:
                            m.setIncY(-2);
                            m.setIncX(-2);
                            m.setImagem("img/meteoro-subindo-direita.gif");
                            m.setY(600);
                            break;
                        default:
                            break;
                    }

                    m.setX(r.nextInt(getWidth()));

                    //Adiciona √† lista de meteoros
                    meteoros.add(m);
                }

                //Desenha meteoros
                for (Meteoro m : meteoros) {
                    m.desenhar(g);
                }

                //Move meteoros
                for (Meteoro m : meteoros) {
                    m.mover();
                }

                //Colis√£o jogador - meteoro
                for (Meteoro m : meteoros) {
                    if (j.colisao(m)) {
                        j.vida--;
                        j.morto++;
                        m.setY(-1);
                        e.setX(j.getX());
                        e.setY(j.getY());
                        lixos.add(m);
                    }
                }

                g.setColor(Color.WHITE);
                g.setFont(new Font("Arial", 0, 19));
                g.drawString("PONTUA√á√ÉO: " + pontuacao, 20, 60);

                for (int x = 1; x <= j.vida; x++) {
                    Imagem vida = new Imagem("img/vida.png");
                    vida.setX(-20 + (x * 43));
                    vida.setY(640);
                    vida.desenhar(g);
                }

                for (int k = j.morto; k > 0; k--) {
                    Imagem morto = new Imagem("img/morto.png");
                    morto.setX(-20 + ((k + j.vida) * 43));
                    morto.setY(640);
                    morto.desenhar(g);
                }
                
                //Colis√£o jogador com lateral
                j.trataColisao(getWidth(), getHeight());
                
                //Colis√£o meteoro - meteoro
                for (int m = 0; m < meteoros.size(); m++) {
                    for (int m2 = meteoros.size() - 1; m2 > 0; m2--) {

                        Meteoro met1 = meteoros.get(m);
                        Meteoro met2 = meteoros.get(m2);

                        if (met1.colisao(met2)) {
                            e.setX(met1.x);
                            e.setY(met2.y);
                            lixos.add(met1);
                            lixos.add(met2);
                        }
                    }
                }

                //Desenha anima√ß√£o de explos√£o
                e.desenhar(g);
                tempo++;

                if (tempo == 110) {
                    e.setY(-400);
                    tempo = 0;
                }

                //Trata colis√£o de meteoros no cen√°rio
                for (Meteoro m : meteoros) {
                    Colisao aux = m.trataColisao(getWidth(), getHeight());
                    if (aux == Colisao.DOWN || aux == Colisao.RIGHT || aux == Colisao.UP || aux == Colisao.LEFT) {
                        lixos.add(m);
                    }
                }

                //Trata meteoros colididos com o cen√°rio
                meteoros.removeAll(lixos);
                lixos.clear();

                //Contador de pontua√ß√£o
                pontuacao++;

                //Gera valor de propor√ß√£o em pontua√ß√£o
                if (pontuacao == 5000) {
                    proporcao = 280;
                } else if (pontuacao == 7500) {
                    proporcao = 250;
                } else if (pontuacao == 10000) {
                    proporcao = 200;
                } else if (pontuacao == 15000) {
                    proporcao = 180;
                } else if (pontuacao == 20000) {
                    proporcao = 150;
                } else if (pontuacao == 25000) {
                    proporcao = 100;
                }

                //Reinicia as configura√ß√µes do jogo ao perder todas as vidas
                if (j.vida == 0 && j.morto == 3) {
                    iniciarJogo = false;
                    fimJogo = true;
                    b.desenhar(g);

                    if (pontuacao > melhorPontuacao) {
                        melhorPontuacao = pontuacao;
                    }

                    o.desenhar(g);
                    g.setColor(Color.WHITE);
                    g.setFont(new Font("Arial", 0, 24));
                    g.drawString("FIM DE JOGO", 415, 180);
                    g.setFont(new Font("Arial", 0, 20));
                    g.drawString("Melhor pontua√ß√£o:", 415, 380);
                    g.drawString("" + melhorPontuacao, 475, 410);
                    g.drawString("Sua pontua√ß√£o:", 425, 450);
                    g.drawString("" + pontuacao, 475, 480);
                    g.drawString("Pressione ESPA√áO para reiniciar", 355, 540);

                    meteoros.clear();
                    lixos.clear();
                    pontuacao = 0;
                    tempo = 0;
                    proporcao = 700;
                    j.vida = 3;
                    j.morto = 0;
                    j.setX(450);
                    j.setY(350);
                    e.setY(700);
                }
            }

            g.dispose();
            getBufferStrategy().show();
            try {
                Thread.sleep(2);
            } catch (InterruptedException ex) {
            }
        }
    }

